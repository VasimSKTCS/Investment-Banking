package Logical_Loop9;

public class S20 {
	public static void main(String[] args) {
		System.out.println("Number Before Swapping");
		int a=50;
		int b=60;
		
		System.out.println("Number After Swappping");
		int c;
		c=a;
		a=b;
		b=c;
		System.out.println("After Swapping"+a+"AND"+b);
		
	}

}
