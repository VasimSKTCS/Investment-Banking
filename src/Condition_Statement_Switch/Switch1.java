package Condition_Statement_Switch;

    public class Switch1 {//Class Is Open
    	
	public static void main(String[] args) {//Method Class Is Open
		
	switch ("Idli") {
	case "Idli": {
	System.out.println("On Monday");
	break;
	}
	case "IDILI": {
	System.out.println("On Tuesday");
	break;
	}
	case "Dosa": {
	System.out.println("On Wednesday");
	break;
	}
	case "Samosa": {
	System.out.println("On Thursday");
	break;
	}
	case "Wada": {
	System.out.println("On Friday");
	break;
	}
	case "Lemon Rice": {
	System.out.println("On Saturday");
	break;
	}
	default:{
	System.out.println("No Breakfast");
	break;
	}
	}
		
	}//Method Class Is Close
	
    }//Class Is Close
