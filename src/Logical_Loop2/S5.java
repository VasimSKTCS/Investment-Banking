package Logical_Loop2;

    public class S5 {
	public static void main(String[] args) {
		
	System.out.println("Calculating Factorial Of 5");
		
	int num=5;
	int fact=1;
		
	for(int i=1;i<=num;i++) {
	fact=fact*i;
	System.out.println(fact);
	}
	
	//System.out.println(fact);
			
	}
    }
