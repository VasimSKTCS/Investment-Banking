package Logical_Loop2;

    public class S7 {
	public static void main(String[] args) {
		
	System.out.println("If We Want To Add Number From 1To20");
		
	int num=20;
	int add=1;
		
	for(int i=1;i<=num;i++) {
	add=add+i;
	System.out.println(add);
	}
	System.out.println(add);
	}
    }
