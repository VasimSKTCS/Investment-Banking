package Method_Without_Parameter;

    public class Sample1 {//Method Class Is Open
	public static void main (String[] args) {//Main Method Class Is Started
		
	System.out.println("Jai Maharashtra Java"); 
	
	Addition();
	
	System.out.println("Jai Hind Java");
		
	}//Main Method Class Is Close
	
    public static void Addition () {
    	
    int a=10;
    int b=20;
    int c=a+b;
    	
    System.out.println(c);
    	
    }

    }//Method Class Is Close
