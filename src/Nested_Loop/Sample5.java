package Nested_Loop;

    public class Sample5 {//Class Body Is Open
	public static void main(String[] args) {//Main Method Body Open
		
	for(int i=1;i<=3;i++) {
	for(int j=1;j<=4;j++) {
		
	if(i==1||i==1||j==3||j==4) {
	System.out.print("*");				
    }
	
	else {
	System.out.println(" ");
	}
	}
	
	System.out.println();
	}
	
	}//Main Method Body Closed
    }//Class Body Is Closed
