package Nested_Loop;

    public class Sample3 {
	public static void main(String[] args) {
		
	System.out.println("Want To Print 5 rows And 5 columns Of Stars ");
		
	for(int i=1;i<=5;i++) {
	for(int j=1;j<=5;j++) {
	System.out.print("*");
	}
	System.out.println();	
	}
		
    }
    }
